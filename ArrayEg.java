

package arrayeg;
public class ArrayEg {
    
    public static void main(String[] args) {
        
        int arr[] = {10,20,30,40,50};
        
        // Traversing over arr[]
            //i = 0   5 < 5             4+1 = 5
        for (int i = 0; i < arr.length; i++) {
            
                    // arr[4]
            System.out.println(arr[i] + " ");
        }
    }
    
}
